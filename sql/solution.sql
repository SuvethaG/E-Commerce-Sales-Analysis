WITH cte as(SELECT product_name,SUM(quantity) total_quantity_sold,SUM(sales_value) as total_sales_value,
DENSE_RANK() OVER(ORDER BY SUM(sales_value) DESC) rn
FROM products p
JOIN order_items o 
ON p.product_id=o.product_id
GROUP BY product_name)

SELECT product_name,total_quantity_sold,total_sales_value
FROM cte
WHERE rn<=10;

 
/*
2. Revenue by Category
Calculate total revenue generated by each product category.
Challenge: Include the percentage contribution of each category to total revenue.
*/

WITH cte as(SELECT c.category_id,category_name,ROUND(SUM(quantity*price)::numeric,2) total_revenue
FROM products p
LEFT JOIN order_items o
ON p.product_id=o.product_id
JOIN category c
ON c.category_id=p.category_id
GROUP BY c.category_id,category_name)

SELECT category_id,category_name,total_revenue,
ROUND((total_revenue/(SELECT SUM(total_revenue) FROM cte))*100.0::numeric,2) percent_of_total_sales
FROM cte
ORDER BY category_id;


/*
3. Average Order Value (AOV)
Compute the average order value for each customer.
Challenge: Include only customers with more than 5 orders.
*/

-- AOV = TR/No of orders

WITH cte AS(SELECT customer_id,SUM(sales_value) total_sales_value,COUNT(customer_id) no_of_orders
FROM orders o
JOIN order_items oi
ON o.order_id=oi.order_id
GROUP BY customer_id
HAVING COUNT(customer_id)>5)

SELECT c.customer_id,first_name,last_name,round((total_sales_value/no_of_orders)::numeric,2) total_order_value,no_of_orders
FROM cte c
LEFT JOIN customers cu
ON cu.customer_id=c.customer_id
ORDER BY c.customer_id;

/*
4. Monthly Sales Trend
Query monthly total sales over the past year.
Challenge: Display the sales trend, grouping by month, return current_month sale, last month sale!
*/
WITH t1 AS
(SELECT substring(order_date::TEXT from 1 for 7) month_year,
ROUND(SUM(sales_value)::NUMERIC,2) sales_value_per_month 
FROM order_items oi
JOIN orders o ON oi.order_id = o.order_id
WHERE order_date > (CURRENT_DATE - INTERVAL '1 year')::DATE 
GROUP BY 1 
ORDER BY 1)

SELECT month_year, sales_value_per_month AS current_month_sales,
LAG(sales_value_per_month) OVER(ORDER BY month_year) previous_month_sales,
ROUND(((sales_value_per_month / LAG(sales_value_per_month) OVER(ORDER BY month_year)) -1)*100, 2) percent_change
FROM t1

/*
