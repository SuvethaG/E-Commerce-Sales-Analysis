/*
11. Top Performing Sellers
Find the top 5 sellers based on total sales value.
Challenge: Include both successful and failed orders, and display their percentage of successful orders.
*/


SELECT s.seller_id,seller_name,round(SUM(sales_value)::numeric,2) total_sales_value,
COUNT(CASE WHEN order_status='Completed' THEN 1 END)  successful_orders,
COUNT(CASE WHEN order_status='Returned' OR order_status='Cancelled' THEN 1 END) failed_orders,
round((COUNT(CASE WHEN order_status='Completed' THEN 1 END)*1.0)/COUNT(order_status)*100.0::numeric,2) success_percent
FROM sellers s JOIN orders o ON o.seller_id=s.seller_id
JOIN order_items oi on oi.order_id=o.order_id
GROUP BY 1
ORDER BY 3 DESC
LIMIT 5;

